const tabIds = ["overview", "entities", "theoretical", "conceptual", "encoding", "rdf", "validation", "presentation", "files"];
const tabButtons = [...document.querySelectorAll('[role="tab"]')];
const panels = [...document.querySelectorAll('.tab-panel')];
const progressLabel = document.getElementById('progressLabel');
const progressFill = document.getElementById('progressFill');
const previousButton = document.getElementById('previousButton');
const nextButton = document.getElementById('nextButton');
let activeTab = 'overview';

function normalizeTab(id) { return tabIds.includes(id) ? id : 'overview'; }
function showTab(id, options = {}) {
  id = normalizeTab(id);
  activeTab = id;
  tabButtons.forEach(btn => {
    const selected = btn.dataset.tab === id;
    btn.setAttribute('aria-selected', String(selected));
    btn.tabIndex = selected ? 0 : -1;
    if (selected) btn.scrollIntoView({behavior:'smooth', block:'nearest', inline:'center'});
  });
  panels.forEach(panel => {
    const selected = panel.id === id;
    panel.hidden = !selected;
    panel.classList.toggle('is-active', selected);
  });
  const index = tabIds.indexOf(id);
  progressLabel.textContent = `Section ${index + 1} of ${tabIds.length}`;
  progressFill.style.width = `${((index + 1) / tabIds.length) * 100}%`;
  previousButton.disabled = index === 0;
  nextButton.disabled = index === tabIds.length - 1;
  nextButton.textContent = index === tabIds.length - 1 ? 'End of presentation' : 'Next →';
  if (!options.fromHistory) history.pushState({tab:id}, '', `#${id}`);
  if (!options.noScroll) window.scrollTo({top: 0, behavior: 'smooth'});
}

tabButtons.forEach(btn => btn.addEventListener('click', () => showTab(btn.dataset.tab)));
document.querySelectorAll('[data-go]').forEach(btn => btn.addEventListener('click', () => showTab(btn.dataset.go)));
previousButton.addEventListener('click', () => { const i=tabIds.indexOf(activeTab); if(i>0) showTab(tabIds[i-1]); });
nextButton.addEventListener('click', () => { const i=tabIds.indexOf(activeTab); if(i<tabIds.length-1) showTab(tabIds[i+1]); });
window.addEventListener('popstate', event => showTab(event.state?.tab || location.hash.slice(1), {fromHistory:true}));
window.addEventListener('hashchange', () => showTab(location.hash.slice(1), {fromHistory:true, noScroll:true}));
document.addEventListener('keydown', event => {
  if (['INPUT','TEXTAREA'].includes(document.activeElement.tagName)) return;
  if (event.key === 'ArrowRight') nextButton.click();
  if (event.key === 'ArrowLeft') previousButton.click();
});

// Entity catalogue
const entityGrid = document.getElementById('entityGrid');
const entityFilters = document.getElementById('entityFilters');
const entitySearch = document.getElementById('entitySearch');
const types = ['All', ...new Set(PROJECT_DATA.entities.map(item => item.type))];
let entityType = 'All';
function renderFilters() {
  entityFilters.innerHTML = types.map(type => `<button type="button" class="${type===entityType?'is-active':''}" data-type="${type}">${type}</button>`).join('');
  entityFilters.querySelectorAll('button').forEach(button => button.addEventListener('click', () => {entityType=button.dataset.type; renderFilters(); renderEntities();}));
}
function renderEntities() {
  const q = entitySearch.value.trim().toLowerCase();
  const items = PROJECT_DATA.entities.filter(item => (entityType==='All'||item.type===entityType) && (!q || `${item.name} ${item.description}`.toLowerCase().includes(q)));
  entityGrid.innerHTML = items.length ? items.map(item => `<article class="entity-card">
    <div class="entity-top"><span class="entity-icon">${ICONS[item.type]||''}</span><div><span class="entity-type">${item.type}</span><h3>${item.name}</h3></div></div>
    <div class="entity-body"><p>${item.description}</p><div class="entity-links">${item.wikidata?`<a href="${item.wikidata}" target="_blank" rel="noopener">Wikidata</a>`:''}${item.wikipedia?`<a href="${item.wikipedia}" target="_blank" rel="noopener">Wikipedia</a>`:''}</div></div>
  </article>`).join('') : '<p>No entities match this filter.</p>';
}
entitySearch.addEventListener('input', renderEntities);
renderFilters(); renderEntities();

